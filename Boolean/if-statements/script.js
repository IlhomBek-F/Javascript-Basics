function falseOrTrue(isItTrue) {
  if (isItTrue) {
    return "Yes, it is true";
  }

  return "No , it is false";
}

console.log(falseOrTrue(true)); // result ---> Yes it is true;

function trueOrFalse(wasThatTrue) {
  if (wasThatTrue) {
    return "Yes that was true";
  }

  return "No, that was false";
}

console.log(trueOrFalse(false)); // result --> No , that was false
